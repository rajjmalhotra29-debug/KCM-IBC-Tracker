// Listed companies -> Screener (ratios). Private -> VCCEdge + Tofler (free filings).
function researchLinks(t) {
  const name = t.name || "";
  const q = encodeURIComponent(name);
  const isPrivate = /\bPVT\b|\bPRIVATE\b/i.test(name);
  const links = [];
  if (isPrivate) {
    links.push({ cls: "tof", label: "Tofler", typ: "private", url: `https://www.tofler.in/search?query=${q}` });
    links.push({ cls: "", label: "VCCEdge", typ: "sub", url: `https://www.vccedge.com/` });
  } else {
    links.push({ cls: "scr", label: "Screener", typ: "listed", url: `https://www.screener.in/full-text-search/?q=${q}` });
    links.push({ cls: "tof", label: "Tofler", typ: "filings", url: `https://www.tofler.in/search?query=${q}` });
  }
  return `<div class="reslinks"><span class="reslabel">Financials</span>${links.map(l =>
    `<a class="reslink ${l.cls}" href="${l.url}" target="_blank" rel="noopener" title="Open ${esc(name)} on ${l.label}">${l.label} <span class="typ">${l.typ}</span><span class="ar">↗</span></a>`
  ).join("")}</div>`;
}

