// ===== render =====
function render() {
  if (!DATA) return;
  let rows = DATA.opportunities.slice();
  if (q) { const k = q.toLowerCase(); rows = rows.filter(o => (o.target.name + " " + o.target.sector + " " + o.target.applicant).toLowerCase().includes(k)); }
  if (fStage) rows = rows.filter(o => fStage === "liq" ? o.target.is_liq : !o.target.is_liq);
  if (fSector) rows = rows.filter(o => (o.target.sector || "").toLowerCase().includes(fSector.toLowerCase()));
  if (fMatched) rows = rows.filter(o => o.match_count > 0);
  $("fClear").style.display = (fStage || fSector || fMatched) ? "inline-block" : "none";
  if (sortMode === "bid") rows.sort((a, b) => { const va = bidDays(a.target) ?? -1, vb = bidDays(b.target) ?? -1; return vb - va; });
  else rows.sort((a, b) => { const sa = a.matches[0]?.score ?? a.match_count ?? 0, sb = b.matches[0]?.score ?? b.match_count ?? 0; return sb - sa; });

  let soon = 0, early = 0, liq = 0;
  rows.forEach(o => { const k = stageInfo(o.target).key; if (k === "soon") soon++; else if (k === "early") early++; else liq++; });
  const visible = rows.slice(0, shown);
  $("hiddenNote").innerHTML = `Showing ${visible.length} of ${rows.length} · sorted by <b>${sortMode === "bid" ? "days to bid (most→least)" : "best fit"}</b> &nbsp;·&nbsp; <b style="color:var(--amber)">${soon} approaching Form G</b> · ${early} early-stage · ${liq} liquidation`;

  if (!rows.length) { $("list").innerHTML = `<div class="opp" style="padding:30px;text-align:center;color:var(--faint)">No opportunities match.</div>`; return; }

  $("list").innerHTML = visible.map((o, i) => {
    const t = o.target, si = stageInfo(t), bd = bidDays(t);
    const matchesHtml = o.locked
      ? lockedPanel(o)
      : (o.matches.length
        ? `<div class="mlabel">Matched clients · ${o.matches.length} (ranked by rule-based fit)</div>${o.matches.map(matchRow).join("")}`
        : `<div class="mlabel">Matched clients</div><div class="locked" style="border-style:solid;background:var(--grey-soft);border-color:var(--line2)"><div class="lks">No client in the book matches this sector yet.</div></div>`);
    return `<div class="opp" style="animation-delay:${i * 55}ms">
      <div class="opp-head">
        <div class="opp-id">
          <div class="opp-name">${esc(t.name)} <span class="stagechip ${si.cls}">${si.label}${t.is_liq ? "" : (bd !== null ? " · ~" + bd + "d to bid" : "")}</span></div>
          <div class="vchain"><b>Sector ·</b> ${esc(t.sector || "inferred from filing")}</div>
          <div class="applicant">Applicant: ${esc(t.applicant || "—")}</div>
          ${researchLinks(t)}
        </div>
        <div class="opp-tags">
          <span class="stage ${t.stage_class}">${esc(t.stage_label || t.status)}</span>
          <span class="deadline">Claims <span class="dl-pill ${dlClass(claimsDays(t))}">${dlText(t)}</span></span>
          <a class="formg-btn" href="${esc(t.source_url || 'https://www.ibbi.gov.in/en/public-announcement')}" target="_blank" rel="noopener">Find on IBBI ↗</a>
          <span class="rp">${esc(t.resolution_professional)}</span>
        </div>
      </div>
      <div class="panels">${formgPanel(t)}${finPanel(t)}</div>
      <div class="matches">${matchesHtml}</div>
      ${engageBar(t)}
    </div>`;
  }).join("");

  const moreEl = $("loadMore");
  if (rows.length > shown) {
    moreEl.style.display = "block";
    moreEl.querySelector("button").textContent = `Load more · ${rows.length - shown} remaining`;
  } else {
    moreEl.style.display = "none";
  }
}
