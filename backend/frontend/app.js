function openDeals() {
  const tr = DATA.track_record || {};
  const isAdmin = me && me.is_admin;
  $("dealsAdmin").style.display = isAdmin ? "block" : "none";
  const body = $("dealsBody");
  if (tr.locked) {
    body.innerHTML = `<div class="deal-locked"><div class="lkt" style="font-weight:700;color:var(--teal-d);margin-bottom:4px">${tr.deals_closed} closed mandate${tr.deals_closed===1?"":"s"} · ${fmtCr(tr.value_fmv_inr_cr)} value created</div>
      <div class="lks" style="color:var(--muted);font-size:13px">The companies and deal details are a premium view.</div>
      <button class="btn amber" style="margin-top:12px" onclick="closeModal('dealsModal'); ${me ? 'openUpgrade()' : "openAuth('signup')"}">${me ? "Unlock details" : "Sign up to view"}</button></div>`;
  } else if (!tr.deals || !tr.deals.length) {
    body.innerHTML = `<div class="bs" style="color:var(--muted)">No mandates recorded yet.</div>`;
  } else {
    body.innerHTML = tr.deals.map(d => `<div class="deal-row">
      <div><div class="dc">${esc(d.client_name)}${d.target_name ? ` <span style="color:var(--faint)">←</span> ${esc(d.target_name)}` : ""}</div>
        <div class="dt">${esc(d.service_type)}</div>
        <div class="dmeta">${esc(d.closed_on)}${d.notes ? " · " + esc(d.notes) : ""}${isAdmin ? ` · <span class="mini" onclick='editDeal(${d.id})' style="cursor:pointer;text-decoration:underline">edit</span> <span class="mini del" onclick='delDeal(${d.id})' style="cursor:pointer;text-decoration:underline;color:var(--red)">delete</span>` : ""}</div>
      </div>
      <div class="df">${fmtCr(d.fmv_inr_cr)}</div>
    </div>`).join("");
  }
  openModal("dealsModal");
}
let editingDealId = null;
function openDealForm() { editingDealId = null; $("dfTitle").textContent = "Add mandate"; ["dfClient","dfTarget","dfService","dfFmv","dfDate","dfNotes"].forEach(i=>$(i).value=""); $("dfErr").textContent=""; openModal("dealFormModal"); }
function editDeal(id) {
  const d = (DATA.track_record.deals || []).find(x => x.id === id); if (!d) return;
  editingDealId = id; $("dfTitle").textContent = "Edit mandate";
  $("dfClient").value=d.client_name; $("dfTarget").value=d.target_name; $("dfService").value=d.service_type; $("dfFmv").value=d.fmv_inr_cr; $("dfDate").value=d.closed_on; $("dfNotes").value=d.notes;
  openModal("dealFormModal");
}
async function saveDeal() {
  const body = { client_name:$("dfClient").value.trim(), target_name:$("dfTarget").value, service_type:$("dfService").value||"Acquisition & structuring", fmv_inr_cr:parseFloat($("dfFmv").value)||0, closed_on:$("dfDate").value, notes:$("dfNotes").value };
  if (!body.client_name) { $("dfErr").textContent="Client is required."; return; }
  try { if (editingDealId) await api("/api/deals/"+editingDealId,{method:"PUT",body:JSON.stringify(body)}); else await api("/api/deals",{method:"POST",body:JSON.stringify(body)}); closeModal("dealFormModal"); toast("Saved"); await load(); openDeals(); }
  catch(e){ $("dfErr").textContent=e.message; }
}
async function delDeal(id) { if(!confirm("Delete this mandate?"))return; await api("/api/deals/"+id,{method:"DELETE"}); toast("Deleted"); await load(); openDeals(); }

async function load() {
  // Dynamic (FastAPI) first; on a static host (GitHub Pages) fall back to data.json.
  try { DATA = await api("/api/dashboard"); window.STATIC = false; }
  catch (e) { DATA = await (await fetch("data.json", { cache: "no-store" })).json(); window.STATIC = true; }
  applyBrand(); setSrc(); setProv(); renderStats(); renderTrackRecord(); populateSectors(); render(); renderAuth();
}
